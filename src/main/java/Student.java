public class Student {
    private int age;
    private int matrikelNr;
    private String name;

    int getAge(){
        return age;
    }
    void setAge(int age){
        this.age = age;
    }
    int getMatrikelNr(){
        return matrikelNr;
    }
    void setMatrikelNr(int matrikelNr){
        this.matrikelNr = matrikelNr;
    }
    String getName(){
        return name;
    }
    void setName(String name){
        this.name = name;
    }
}
